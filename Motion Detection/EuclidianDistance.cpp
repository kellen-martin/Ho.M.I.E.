# include <iostream>
# include <opencv2/opencv.hpp>
# include <cmath>

using namespace std;
using namespace cv;

double EuclidianDistance(double dx, double dy, double dz) {
// distance equation
double distance;
distance = sqrt(pow(dx, 2) + pow(dy, 2) + pow(dz, 2));

return distance;
}

